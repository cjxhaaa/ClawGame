import os

OUT_DIR = "apps/web/public/assets"

def generate_optimized_svg(ascii_art, palette, filename):
    lines = [line.strip() for line in ascii_art.strip().split('\n')]
    scale = 10
    
    # Base is 16x32
    # 16 * 10 = 160. Offset = (256-160)/2 = 48
    # 32 * 8 = 256. (Wait, let's use scale 8. 16*8 = 128, offset 64. 32*8 = 256, offset 0)
    scale = 8
    offset_x = 64
    offset_y = 0
    
    svg_rects = []
    for y, line in enumerate(lines):
        if ':' in line:
            line = line.split(':')[1].strip()
        if not line: continue
        
        x = 0
        while x < len(line):
            char = line[x]
            if char == '.':
                x += 1
                continue
            
            run_length = 1
            while x + run_length < len(line) and line[x + run_length] == char:
                run_length += 1
            
            if char in palette:
                color = palette[char]
                rx = offset_x + x * scale
                ry = offset_y + y * scale
                rw = run_length * scale
                rh = scale
                svg_rects.append(f'  <rect x="{rx}" y="{ry}" width="{rw}" height="{rh}" fill="{color}" />')
            x += run_length

    svg = f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 256 256" width="256" height="256" shape-rendering="crispEdges">\n'
    svg += '\n'.join(svg_rects)
    svg += '\n</svg>'
    with open(filename, 'w') as f:
        f.write(svg)

